// AB个罐子，分别放红蓝球（标有 A、B），各三个. 把球混在一起后，连续抽 3 个，计算：A、B 球至少有一个，且红球至少一个的概率。

// 思路: 1. 计算总共的可能性 2. 计算不符合条件的可能性 3. 用总可能性减去不符合条件的可能性
// 总可能性: C(6, 3) = 20
// 不符合条件的可能性: C(3, 3) + C(3, 3) = 2
// 结果: 20 - 2 = 18, 即 18/20 = 0.9
// 至少一个红球，即 1 - 两个罐子都没有红球的概率
// 两个罐子都没有红球的概率: C(3, 3) / C(6, 3) = 1 / 20 = 0.05
// 时间复杂度: O(1)
